void matrix_multiplier(int len, int set1, int set2, float first_mat[][transformation_lenght], float second_mat[][1], float mult_mat[][1]) {
  int i,j,k;
  for (i = 0; i < len; ++i) {
      for (j = 0; j < set2; ++j) {
          mult_mat[i][j] = 0;
      }
  }
  for (i = 0; i < len; ++i) {
      for (j = 0; j < set2; ++j) {
          for (k = 0; k < set1; ++k) {
              mult_mat[i][j] += first_mat[i][k] * second_mat[k][j];   
          }
      }
  }
}

void print_float(float value)
{
    int temp,temp2;
    char flag = ' ';
    temp = (int)value;
    temp2 = (int)((value-temp)*10000);
    if(temp < 0 || temp2 < 0){
      flag = '-';
    }
    printf("The result is: %c%d.%04d\n",flag,abs(temp),abs(temp2));
}

